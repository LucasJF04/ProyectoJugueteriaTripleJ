<script>
document.addEventListener('DOMContentLoaded', function() {
    // Escucha el evento de clic en los botones de editar
    document.querySelectorAll('.btnEditar').forEach(function(button) {
        button.addEventListener('click', function() {
            var id = this.getAttribute('data-id');
            // Llama a una función para cargar los datos del usuario
            cargarDatosUsuario(id);
        });
    });

    function cargarDatosUsuario(id) {
        fetch('<?= base_url('usuarios/obtenerDatos/'); ?>' + id)
            .then(response => response.json())
            .then(data => {
                if (!data.error) {
                    // Rellena el formulario del modal con los datos del usuario
                    document.getElementById('idUsuario').value = data.id;
                    document.getElementById('nombre').value = data.nombre;
                    document.getElementById('apellido_Paterno').value = data.apellido_Paterno;
                    document.getElementById('apellido_Materno').value = data.apellido_Materno;
                    document.getElementById('email').value = data.email;
                    document.getElementById('rol').value = data.rol;
                } else {
                    console.error(data.error);
                }
            })
            .catch(error => console.error('Error:', error));
    }
});

</script>


